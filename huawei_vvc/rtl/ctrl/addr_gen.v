//===========================================================================
// addr_gen.v - 地址生成器
// 功能: 生成输入写、列读、列写、行读、行输出的地址
// 支持非正方形TU
//===========================================================================

module addr_gen (
    input  wire        clk,
    input  wire        rst_n,

    // TU尺寸配置
    input  wire [6:0]  tu_width,
    input  wire [6:0]  tu_height,

    // 输入写地址
    input  wire        input_wr_en,
    input  wire [11:0] input_addr_in,
    output wire [11:0] input_addr_out,

    // 列变换读地址
    output wire [11:0] col_rd_addr,
    input  wire        col_rd_en,
    input  wire [6:0]  col_idx,
    input  wire [6:0]  row_idx,

    // 列变换写地址（中间buffer）
    output wire [11:0] col_wr_addr,

    // 行变换读地址
    output wire [11:0] row_rd_addr,

    // 输出读地址
    output wire [11:0] output_rd_addr,
    input  wire        output_rd_en,
    input  wire [11:0] output_idx
);

    //===========================================================================
    // 输入写地址 - 直接透传
    //===========================================================================
    assign input_addr_out = input_addr_in;

    //===========================================================================
    // 列变换读地址计算
    // 列变换: 读取同一列的所有行
    // addr = row * tu_width + col
    //===========================================================================
    wire [11:0] col_idx_ext = {5'b0, col_idx};  // 扩展到12bit
    assign col_rd_addr = row_idx * tu_width + col_idx_ext;

    //===========================================================================
    // 列变换写地址（到中间buffer）
    // 按行存储: addr = row * tu_width + col
    //===========================================================================
    assign col_wr_addr = col_rd_addr;

    //===========================================================================
    // 行变换读地址计算（从中间buffer）
    // 行变换: 读取同一行的所有列
    //===========================================================================
    assign row_rd_addr = row_idx * tu_width + col_idx_ext;

    //===========================================================================
    // 输出读地址
    //===========================================================================
    assign output_rd_addr = output_idx;

endmodule
